const lines = rawData.split('\n').filter(Boolean);
const updates = {};

for (const line of lines) {
  let [wordPart, defPart] = line.split('—').map(s => s.trim());
  
  if (wordPart.includes(' / ')) {
    const parts = wordPart.split(' / ');
    updates[parts[0]] = { wordPart: parts[0], defPart };
    updates[parts[1]] = { wordPart: parts[1], defPart };
  } else {
    updates[wordPart] = { wordPart, defPart };
  }
}

for (const key of Object.keys(updates)) {
  const { defPart } = updates[key];
  const items = defPart.split(';');
  const poses = [];
  const meanings = [];

  for (const item of items) {
    const [pos, meaning] = item.split(':').map(s => s.trim());
    if (pos) poses.push(pos);
    if (meaning) meanings.push(meaning);
  }
  
  updates[key].partOfSpeech = poses.join(', ');
  updates[key].meanings = meanings.map(m => m.split(',').map(s => s.trim()).join(', ')).join(', ');
}

// read source
const file_path = 'src/data/initialMasteryMap.ts';
let code = fs.readFileSync(file_path, 'utf8');

// Update interface
code = code.replace(
  `{ word: string; partOfSpeech: string; frequencyRank: number; status: MasteryStatus; type: 'word' | 'grammar'; weight: MasteryWeight; sessionNotes: string }[]`,
  `{ word: string; partOfSpeech: string; meanings: string; frequencyRank: number; status: MasteryStatus; type: 'word' | 'grammar'; weight: MasteryWeight; sessionNotes: string }[]`
);

// process objects
code = code.replace(/{ word: "([^"]+)", partOfSpeech: "([^"]+)",([^}]+)}/g, (match, word, pos, rest) => {
  if (updates[word]) {
    const updated = updates[word];
    return `{ word: "${word}", partOfSpeech: "${updated.partOfSpeech}", meanings: "${updated.meanings.replace(/"/g, '\\"')}",${rest}}`;
  }
  // add empty meanings for missing ones so we don't break type
  return `{ word: "${word}", partOfSpeech: "${pos}", meanings: "",${rest}}`;
});

fs.writeFileSync(file_path, code);
